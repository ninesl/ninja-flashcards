<template>
  <div class="study-report">
    <div class="report-controls no-print">
      <button class="print-btn" @click="printReport">Print Report</button>
      <button class="back-btn" @click="goBack">Back</button>
    </div>

    <div class="report-content">
      <div class="report-header">
        <h1 class="report-title">Ninja Flashcards Study Progress Report</h1>
        <div class="report-info">
          <p><strong>User:</strong> {{ $store.state.user.username }}</p>
          <p><strong>Generated:</strong> {{ currentDateTime }}</p>
          <p><strong>Total Decks Studied:</strong> {{ reportData.length }}</p>
        </div>
      </div>

      <div v-if="isLoading" class="loading">
        <ninja-loader/>
      </div>

      <div v-else class="report-table-container">
        <table class="report-table">
          <thead>
            <tr>
              <th>Deck Name</th>
              <th>Genre/Subject</th>
              <th>Score</th>
              <th>Performance</th>
              <th>Last Studied</th>
            </tr>
          </thead>
          <tbody>
            <tr v-for="(record, index) in reportData" :key="index" :class="getRowClass(record.performance)">
              <td class="deck-name">{{ record.deck_name }}</td>

              <td>{{ record.genre }}</td>

              <td class="center">{{ getScoreDisplay(record) }}</td>

              <td class="center">
                <span class="brain-color" :class="getBrainColorClass(record.score)">
                  {{ getBrainColorText(record.score) }}
                </span>
              </td>

              <td class="center">{{ formatDateTime(record.last_updated) }}</td>
            </tr>
          </tbody>
        </table>

        <div class="report-summary">
          <h3>Study Summary</h3>
          <div class="summary-stats">
            <div class="stat-item">
              <span class="stat-label">Average Score:</span>
              <span class="stat-value">{{ averageScore }}/3</span>
            </div>
            <div class="stat-item">
              <span class="stat-label">Excellent Performance:</span>
              <span class="stat-value">{{ excellentCount }} decks</span>
            </div>
            <div class="stat-item">
              <span class="stat-label">Good Performance:</span>
              <span class="stat-value">{{ goodCount }} decks</span>
            </div>
            <div class="stat-item">
              <span class="stat-label">Needs Improvement:</span>
              <span class="stat-value">{{ poorCount }} decks</span>
            </div>
          </div>
        </div>
