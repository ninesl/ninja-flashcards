<script>
import DeckService from '@/services/DeckService.js'
import NinjaLoader from '../components/NinjaLoader.vue'

export default {
  name: "study-report",
  components: {
    NinjaLoader
  },
  data() {
    return {
      reportData: [],
      isLoading: true,
      currentDateTime: new Date().toLocaleString()
    }
  },
  computed: {
    averageScore() {
      if (this.reportData.length === 0) return 0;
      const total = this.reportData.reduce((sum, record) => sum + record.score, 0);
      return (total / this.reportData.length).toFixed(1);
    },
    excellentCount() {
      return this.reportData.filter(record => record.performance === 'Excellent').length;
    },
    goodCount() {
      return this.reportData.filter(record => record.performance === 'Good').length;
    },
    poorCount() {
      return this.reportData.filter(record => record.performance === 'Poor').length;
    }
  },
  methods: {
    printReport() {
      window.print();
    },
    goBack() {
      this.$router.go(-1);
    },
    getRowClass(performance) {
      return `row-${performance.toLowerCase()}`;
    },
    getStatusClass(status) {
      switch(status) {
        case 1: return 'private';
        case 2: return 'unlisted';
        case 3: return 'public';
        default: return '';
      }
    },
    getStatusText(status) {
      switch(status) {
        case 1: return 'Private';
        case 2: return 'Unlisted';  
        case 3: return 'Public';
        default: return 'Unknown';
      }
    },
    formatDate(dateString) {
      if (!dateString) return 'Never';
      return new Date(dateString).toLocaleDateString();
    },
    formatDateTime(dateString) {
      if (!dateString) return 'Never';
      const date = new Date(dateString);
      const dateStr = date.toLocaleDateString();
      const timeStr = date.toLocaleTimeString();
      return `${dateStr} ${timeStr}`;
    },
    getPerformance(score) {
      switch(score) {
        case 3: return 'Excellent';
        case 2: return 'Good';
        case 1: return 'Poor';
        default: return 'No Data';
      }
    },
    getScoreText(score) {
      switch(score) {
        case 3: return 'Green';
        case 2: return 'Yellow';
        case 1: return 'Red';
        default: return 'No Data';
      }
    },
    getScoreDisplay(record) {
      // Use actual correct answers from database if available, otherwise estimate
      if (record.correct_answers !== undefined && record.num_cards !== undefined) {
        return `(${record.correct_answers}/${record.num_cards})`;
      }
      const totalCards = record.num_cards || record.card_count || 0;
      if (totalCards > 0) {
        const correct = Math.round((this.getScorePercentage(record.score) / 100) * totalCards);
        return `(${correct}/${totalCards})`;
      }
      return this.getBrainColorText(record.score);
    },
    getScorePercentage(score) {
      switch(score) {
        case 3: return 85; // Green = ~85%
        case 2: return 65; // Yellow = ~65%
        case 1: return 35; // Red = ~35%
        default: return 0;
      }
    },
    getBrainColorText(score) {
      switch(score) {
        case 3: return 'ðŸŸ¢ Green';
        case 2: return 'ðŸŸ¡ Yellow';
        case 1: return 'ðŸ”´ Red';
        default: return 'No Data';
      }
    },
    getBrainColorClass(score) {
      switch(score) {
        case 3: return 'green';
        case 2: return 'yellow';
        case 1: return 'red';
        default: return '';
      }
    },
    async loadReportData() {
      try {
        // Get user's studied decks with history
        const response = await DeckService.getUserStudyReport(this.$store.state.user.id);
        this.reportData = response.data.map(record => ({
          ...record,
          performance: this.getPerformance(record.score)
        }));
        
        setTimeout(() => {
          this.isLoading = false;
        }, 800);
      } catch (error) {
        // eslint-disable-next-line no-console
        console.error('Error loading report data:', error);
        this.isLoading = false;
      }
    }
  },
  created() {
    this.loadReportData();
  }
};
</script>

